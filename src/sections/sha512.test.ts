import { ToSHA512 } from './sha512';

describe('ToSHA512', () => {
    it('empty', () => {
        expect(() => ToSHA512([])).toThrow('string array is empty');
    });
    it('single', () => {
        expect(ToSHA512(['abc'])).toStrictEqual([
            'DDAF35A193617ABACC417349AE20413112E6FA4E89A97EA20A9EEEE64B55D39A2192992A274FC1A836BA3C23A3FEEBBD454D4423643CE80E2A9AC94FA54CA49F',
        ]);
    });
    it('multiple', () => {
        expect(ToSHA512(['abc', 'def'])).toStrictEqual([
            'DDAF35A193617ABACC417349AE20413112E6FA4E89A97EA20A9EEEE64B55D39A2192992A274FC1A836BA3C23A3FEEBBD454D4423643CE80E2A9AC94FA54CA49F',
            '40A855BF0A93C1019D75DD5B59CD8157608811DD75C5977E07F3BC4BE0CAD98B22DDE4DB9DDB429FC2AD3CF9CA379FEDF6C1DC4D4BB8829F10C2F0EE04A66663',
        ]);
    });
});
